    public void ExportarRegistros() {
        ConectarBD conexion = new ConectarBD();
        Connection con = conexion.getConnection();
        String sql = "SELECT * INTO OUTFILE 'C:/ProgramData/MySQL/MySQL Server 8.0/Uploads/Empleados.csv' FIELDS TERMINATED BY ',' LINES TERMINATED BY '\n' FROM Articulo";
        
        try {
            PreparedStatement export = con.prepareStatement(sql);
            export.execute();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e + "No se logró exportar la información.");
        }
    }
    
